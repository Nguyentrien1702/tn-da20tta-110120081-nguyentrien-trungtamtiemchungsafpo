                        <li>Do kỹ thuật tiêm xảy ra sai sót, quá trình chuẩn bị, pha hồi
                            chỉnh, kỹ thuật tiêm, sử dụng Vắc-xin không đúng (tiêm không đúng đường tiêm ví dụ tiêm bắp
                            lại chỉ định tiêm dưới da).</li>
                        <li>Do tâm lý lo sợ khi tiêm, đặc biệt là ở trẻ nhỏ và những người
                            sợ kim tiêm, thường đau đầu chóng mặt, trẻ quấy khóc, la hét. Trẻ quấy khóc nhiều có thể ảnh
                            hưởng đến kỹ thuật tiêm của nhân viên y tế.</li>
                        <li>Do bệnh trùng hợp ngẫu nhiên xảy sau khi tiêm phòng, nguyên nhân
