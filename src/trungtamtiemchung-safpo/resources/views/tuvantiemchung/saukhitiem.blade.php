                    <h6 id="2">2. Các phản ứng thường gặp sau khi chủng ngừa Vắc-xin?</h6>
                    <p>
                        Phản ứng sau tiêm chủng là những hiện tượng bất thường về sức khỏe xảy ra ngay sau tiêm chủng
                        hoặc sau tiêm chủng vài ngày, bao gồm
                        cả phản ứng toàn thân và phản ứng tại vị trí tiêm (với vắc xin dùng đường tiêm). Hầu hết các
                        biểu hiện đều ở mức độ nhẹ (sốt, đau,
                        sưng, ngứa tại vị trí tiêm), một số ít ở mức độ vừa (dị ứng, mày đay), rất hiếm ở mức độ nặng
                        (sốc phản vệ, có thể dẫn đến tử vong).
                    </p>
                    <p>
                        Phản ứng sau tiêm chủng ở mức độ nhẹ:
                    </p>
