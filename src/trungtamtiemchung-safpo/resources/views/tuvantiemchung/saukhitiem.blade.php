                            mà không cần điều trị gì.Trong một số
                            trường hợp, có thể xuất hiện bầm tím tại vị trí tiêm, nhất là ở những trẻ có bệnh lý về máu.
                        </li>
                        <li>
                            Biểu hiện toàn thân: hay gặp nhất là sốt, sốt thường trên dưới 38.5 độ, thường không kéo dài
                            quá 2 ngày, có thể chườm ấm hoặc nếu
                            trẻ sốt quá 38.5 độ thì cho trẻ uống thuốc hạ sốt.
                        </li>
                        <li>
                            Một số trẻ có phản ứng phát ban giả sởi trên da sau tiêm phòng sởi, hoặc nổi vài mụn nước
                            như ban mụn nước thủy đậu sau tiêm phòng
                            thủy đậu. Số lượng các ban và mụn thường ít và thường biến mất sau 1 đến 2 ngày
                        </li>
