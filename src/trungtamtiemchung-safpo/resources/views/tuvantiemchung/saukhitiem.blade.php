                    <h6 id="3">3. Nguyên nhân dẫn đến phản ứng sau tiêm chủng</h6>
                    <p>
                        Ngay cả khi Vắc-xin đảm bảo chất lượng, được xử lý và tiêm chủng đúng cách thì vẫn có thể xảy ra
                        những phản ứng sau tiêm do nhiều nguyên nhân khác nhau, trong đó có những nguyên nhân chủ yếu
                        sau:
                    </p>
