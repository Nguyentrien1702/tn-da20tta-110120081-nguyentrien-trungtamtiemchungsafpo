                        <li>Do chất lượng vắc xin không đảm bảo.</li>
                        <li>Ngoài ra có nhiều trường hợp do cơ địa từng người mà xảy ra phản
                            ứng dị ứng sau tiêm Vắc-xin, có thể nặng nhẹ tùy trường hợp.</li>
