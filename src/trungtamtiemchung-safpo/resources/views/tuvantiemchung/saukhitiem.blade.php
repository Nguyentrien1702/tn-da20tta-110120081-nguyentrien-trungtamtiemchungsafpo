                            bón. Hiện tượng này sẽ tự hết sau 1 đến 2 ngày.
                        </li>
                        <li>
                            Một số trường hợp có hiện tượng giả cúm, với biểu hiện hắt hơi, chảy nước mũi trong, đau
                            đầu,... Những triệu chứng này thường xuất
                            hiện sau khi tiêm Vắc-xin cúm và sẽ hết sau 1 đến 2 ngày.
                        </li>
                    </ul>
