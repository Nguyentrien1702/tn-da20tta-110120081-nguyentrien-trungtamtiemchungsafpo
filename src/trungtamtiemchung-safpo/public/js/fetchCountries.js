// Hàm để thêm các quốc gia vào dropdown list
function populateCountries() {
    const countrySelect = document.getElementById('nuocsx');
    
    // Sắp xếp tên quốc gia theo thứ tự bảng chữ cái
    const sortedCountries = Object.values(vietnameseNames).sort((a, b) => a.localeCompare(b));
    
    // Thêm các quốc gia vào dropdown list
    sortedCountries.forEach(country => {
        const option = document.createElement('option');
        option.value = country; // Sử dụng tên quốc gia làm giá trị
        option.text = country; // Hiển thị tên quốc gia
        countrySelect.appendChild(option);
    });
}